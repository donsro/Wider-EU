<h2>EU Enlargement</h2>
<p>The <a href="http://ec.europa.eu/enlargement/index_en.htm">current EU enlargement position</a> is set out clearly.</p>
<p>Current candidate countries include Macedonia, Montenegro, Serbia and Albania, as well as Turkey and Iceland.</p>
<p>Potential candidate countries are Bosnia-Herzegovina and Kosovo (under UNSCR 1244).</p>