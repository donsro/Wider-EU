<p><strong>Rural Space &amp; Infrastructure</strong> is a consultancy company that works world-wide in partnership with all stakeholders to eliminate poverty, influence change and improve sustainable livelihoods, particularly in post-conflict, cross-border and water dispute areas.</p>

          <p>As a 360 degree development partner, <strong>Rural Space &amp; Infrastructure</strong> strengthens management capacities in transition economies. It also supports institutional development, including reform of public administration in central and local government, as well as facilitates the transparent management of national and donor funds. <strong>Rural Space &amp; Infrastructure</strong> also has experience of cork-screwing upward small scale, NGO-driven, start-up projects for subsequent funding on a larger scale by bilateral/multilateral donors and the private sector.</p>

          <p><strong>Rural Space &amp; Infrastructure</strong> is a knowledge-based company that promotes change management and corporate governance, using innovative and pragmatic solutions. It forms strategic alliances with local consultancy companies and universities in key geographical hubs. This allows <strong>Rural Space &amp; Infrastructure</strong> to hear more clearly local client voices, identify needs and provide demand-driven assistance. It works with donors, NGOs, academics and the private sector to provide inputs to key research and policy programmes that both challenge the causes of poverty, and identify what can be done to eliminate it. <strong>Rural Space &amp; Infrastructure</strong> also facilitates short-burst, highly focussed reviews of strategic options by senior managers and decision-makers.</p>

          <p><strong>Rural Space &amp; Infrastructure</strong> has programme and project management experience of:</p>

          <ul>
            <li>preparing emergency rehabilitation and reconstruction programmes for post-conflict and war-torn economies, particularly their economic and social infrastructures;</li>
            <li>implementing, coordinating and monitoring multilateral and bilateral credit lines and associated technical assistance;</li>
            <li>controlling and coordinating public revenue and expenditure, as well as linking foreign aid requirements and public investment programming;</li>
            <li>designing and implementing sector policy and investment projects for the development and effective management of crop, livestock, forestry and fishery natural resources, as well as creating private property markets through land reform;</li>
            <li>developing national infrastructure projects, covering water supply, irrigation, transport, energy, education, telecommunications, health and housing;</li>
            <li>reforming legal, regulatory, institutional and administrative frameworks;</li>
            <li>liquidating, privatising and right-sizing public/parastatal enterprises, as well as restructuring them by focussing on strategic and total quality management, including accreditation through ISO 9001 and the application of LEAN principles with which to eliminate waste and duplication;</li>
            <li>unravelling disincentives to profitable private sector production, whether SMEs or larger enterprises, and providing appropriate support infrastructures that will help businesses to take advantage of available market opportunities;</li>
            <li>helping countries to prepare for entry to the EU, as well as promotong Wider Europe and European Neighbourhood Policy;</li>
            <li>promoting trade with aid, corporate social responsibility, social entrepreneurship and cross-border cooperation; and</li>
            <li>transferring manpower skills, through the design and delivery of best practice training courses, production of training videos, &quot;twinning&quot; of university departments and business schools, as well as the promotion of distance learning.</li>
