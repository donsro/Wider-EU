<h2>Interns</h2> 
<p>WIDER EUROPE is looking to work with interns living in the different countries of the European Neighbourhood.</p>

<p>Working on a remote access basis, interns will be young professionals and students who are interested in economic, social, environmental, geo-political or media issues relevant to the European Neighbourhood.</p>

<p>For more details about how to work with WIDER EUROPE, please contact Ross Bull (<strong>rossbull@widereurope.eu</strong>).</p>
