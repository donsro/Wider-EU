<p>The outcome of the Greek crisis is important to Moscow for three reasons. First, a new round of the crisis in the EU would affect the Russian economy — which remains closely linked to it — and oil prices. Second, the EU is turning into a large arena of uncertainty no matter whether Greece stays or goes. Third, any weakening of the EU's position could have an impact on the outcome of the situation in Crimea and the east of Ukraine.</p>
<p>Lithuania joined the euro on 1 January 2015, one year after Latvia.</p>
<p>Croatia became the 28th member state of the European Union on 1 July 2013.</p>
<p>On 29 June 2012, Montenegro started EU accession negotiations, barely six years after declaring independence, overtaking Macedonia on the EU accession path.</p>
<p>Serbia was granted candidate status on 1 March 2012, following its application for EU membership in 2009. Negotiations started in January 2014</p>
<p>The EU is reopening membership talks with Turkey, more than three years after freezing negotiations, in an attempt to boost the prospects for democratic reforms.</p>
<p>Lampedusa - closer to Tunisia than Sicily - has become the gateway into the EU for migrants escaping unrest and civil war in the Middle East and Africa.</p>
<p>Brussels now fears there is a 'poverty trap' for half of Europe as the North-South gap widens. Some 19m people are seeking employment across the eurozone.</p>
<p>Greece offers a residence permit to foreigners who buy property valued at €250,000. A non-EU national who buys a €500,000 house in Portugal can gain citizenship after six years. A foreigner who invests £1m in a UK company today can apply for permanent residence five years from now, but must be resident in the UK for six months in each of those years. Malta has declared it will sell a Maltese passport to anyone who pays €650,000.</p>