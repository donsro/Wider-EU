<p>Greece’s economy is on the brink of collapse after capital controls imposed ahead of Sunday’s referendum left the country with shortages of food and drugs, the tourist industry facing a wave of cancellations, and banks with barely enough money to survive the weekend. Banks said they had a €1bn cash buffer to see them through the weekend – equal to just €90 (£64) a head for the 11 million-strong population – and would require immediate help from the European Central Bank on Monday whatever the result of the referendum, in which the two sides are running neck and neck. Alexis Tsipras, Greece’s prime minister, was fighting for his political life on Friday night, using a rally to say that a NO vote would enable him to negotiate a reform-for-debt-relief deal with the country’s creditors. The survival of the Syriza coalition is in doubt as Greece starts to suffer shortages of basic provisions, including the sale of vital drugs in pharmacies nationwide. Food staples, such as sugar and flour, were also fast running out on Friday as consumers started to feel the effect of the restrictions.</p>


