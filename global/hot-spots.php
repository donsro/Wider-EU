<p>There are also Hot Spot organisations that cross borders, such as al-Qaeda, the Taleban, Hamas, Hezbollah and the Muslim Brotherhood. Access to the websites of organisations such as these varies, but the material on them can be unpleasant, (if you can upload it). Further information can be found by searching the web under International Terrorist Entities.</p>
<p>The <a href="http://www.jamestown.org/programs/gta/">Jamestown Foundation</a> offers an in-depth analysis on al-Qaeda and the war on terror.</p>
<p>An interesting research project being implemented by the Norwegian Defence Research Establishment is the <a href="http://www.mil.no/felles/ffi/english/start/research/Analysis_Division/_TERRA/">Transnational Radical Islamism Project</a>. This currently conducts the following thematic research:</p>
<ul>
	<li>Radical Islamist networks on the European continent</li>
	<li>Islamist militancy in Saudi Arabia</li>
	<li>Islamist insurgent groups in Iraq and their external links</li>
	<li>The evolution of radical Islamism in Afghanistan and Pakistan</li>
	<li>Al-Qaida’s "Jihadi strategic studies"-literature and the al-Qaida strategist Abu Mus‘ab al-Suri</li>
	<li>Al-Qaida and Jihadi groups on the Internet</li>
	<li>CBRN-manuals on Jihadi websites</li>
	<li>Terrorist threats to critical infrastructure</li>
	<li>Databases and collection of primary sources on radical Islamism</li>
</ul>